export enum Regex{
    Name = "[a-zA-Z][a-zA-Z ]{2,}",
    Email = "^[a-zA-Z0-9.!#$%&*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$",
    Password = "^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{8,}$",
    Contact = "[0-9]{10}",
    Education = "[A-Z]\.[A-Za-z]|[A-Z]",
    Percent = "(^100(\.0{1,2})?$)|(^([1-9]([0-9])?|0)(\.[0-9]{1,2})?$)",
    School = "[A-Za-z]{1,}[A-Za-z.&() ]{1,}"
}